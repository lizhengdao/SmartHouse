package cn.com.zzwfang.bean;

public class BindBankCardInfoBean extends BaseBean {

	private String BankCity;
	
	private String BankCode;
	
	private String BankImage;
	
	private String BankName;
	
	private String OpenAccountName;
	
	private String RealName;
	
	private String ShowImage;

	public String getBankCity() {
		return BankCity;
	}

	public void setBankCity(String bankCity) {
		BankCity = bankCity;
	}

	public String getBankCode() {
		return BankCode;
	}

	public void setBankCode(String bankCode) {
		BankCode = bankCode;
	}

	public String getBankImage() {
		return BankImage;
	}

	public void setBankImage(String bankImage) {
		BankImage = bankImage;
	}

	public String getBankName() {
		return BankName;
	}

	public void setBankName(String bankName) {
		BankName = bankName;
	}

	public String getOpenAccountName() {
		return OpenAccountName;
	}

	public void setOpenAccountName(String openAccountName) {
		OpenAccountName = openAccountName;
	}

	public String getRealName() {
		return RealName;
	}

	public void setRealName(String realName) {
		RealName = realName;
	}

	public String getShowImage() {
		return ShowImage;
	}

	public void setShowImage(String showImage) {
		ShowImage = showImage;
	}
	
	
}
